"""Use Cases — the front door of the Library.

A Use Case is a real business scenario that composes one or more Library
assets (skills, agents, MCP servers, prompts, capabilities) to solve a
concrete problem.

Storage: per-workspace JSON files under output/library/<ws>/_use_cases/.

Each use case carries:
    - title              (one-liner the user reads first)
    - persona            (who has this problem, with role + company type)
    - industry           (vertical the case is most relevant to)
    - complexity         (quick_win | moderate | advanced)
    - problem            (3-4 sentences of pain — what's broken today)
    - solution           (3-4 sentences of the proposed approach)
    - walkthrough        (numbered steps with concrete detail)
    - tools_used         (list of {category, asset_id} into the Library)
    - outcome_metric     (the quantifiable win — e.g. "12 hrs/week saved")
    - tags
    - ratings / comments (reuse library/store patterns)
    - vetted / source    (who created this; auto-vet if generated by
                          Colaberry under trusted prompt)
"""

from __future__ import annotations

import json
import time
import uuid
from dataclasses import asdict, dataclass, field
from pathlib import Path
from typing import Any

LAYER = "product"
PRODUCT = "library"

ROOT = Path(__file__).resolve().parents[3]
UC_ROOT = ROOT / "output" / "library"


def _now() -> str:
    return time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())


def _uc_dir(workspace: str) -> Path:
    d = UC_ROOT / workspace / "_use_cases"
    d.mkdir(parents=True, exist_ok=True)
    return d


@dataclass
class ToolRef:
    category: str
    asset_id: str
    role: str = ""   # one-liner why this tool is in this use case


@dataclass
class UseCase:
    use_case_id: str
    workspace: str = "global"
    title: str = ""
    summary: str = ""           # one-sentence elevator pitch
    persona: str = ""
    industry: str = ""          # primary vertical
    complexity: str = "moderate"  # quick_win | moderate | advanced
    problem: str = ""
    solution: str = ""
    walkthrough: list[str] = field(default_factory=list)  # numbered steps
    tools_used: list[dict] = field(default_factory=list)   # [{category, asset_id, role}]
    outcome_metric: str = ""
    tags: list[str] = field(default_factory=list)
    # Provenance
    created_at: str = ""
    created_by: str = ""        # "scheduler:bootstrap" / "scheduler:daily" / user
    source: str = "hand-crafted"  # "llm-generated" | "user-submitted" | "hand-crafted"
    generator_meta: dict = field(default_factory=dict)
    # Curation
    vetted: bool = False
    vetted_by: str | None = None
    vetted_at: str | None = None
    vetted_notes: str = ""
    # Tenancy — mirrors AssetMetadata.owning_company_id. "community" means
    # the case is visible to every tenant; a company_id slug scopes it to
    # that single tenant. Default is "community" so seeded global cases
    # stay visible to everyone.
    owning_company_id: str = "community"
    # Aggregates
    rating_avg: float = 0.0
    rating_count: int = 0
    comment_count: int = 0


_KNOWN_FIELDS: set[str] | None = None


def _known() -> set[str]:
    global _KNOWN_FIELDS
    if _KNOWN_FIELDS is None:
        import dataclasses
        _KNOWN_FIELDS = {f.name for f in dataclasses.fields(UseCase)}
    return _KNOWN_FIELDS


def _path(workspace: str, use_case_id: str) -> Path:
    return _uc_dir(workspace) / f"{use_case_id}.json"


def save(uc: UseCase) -> None:
    if not uc.created_at:
        uc.created_at = _now()
    _path(uc.workspace, uc.use_case_id).write_text(
        json.dumps(asdict(uc), indent=2), encoding="utf-8",
    )


def get(workspace: str, use_case_id: str) -> UseCase | None:
    p = _path(workspace, use_case_id)
    if not p.exists():
        return None
    try:
        data = json.loads(p.read_text(encoding="utf-8"))
        cleaned = {k: v for k, v in data.items() if k in _known()}
        return UseCase(**cleaned)
    except Exception:
        return None


def list_all(workspace: str = "global", limit: int | None = None,
                  vetted_only: bool = False, sort: str = "newest",
                  viewer_company_id: str | None = None) -> list[UseCase]:
    """List use cases for a workspace. Sort: newest | rating | persona.

    viewer_company_id (Auth 1): when set, keep only cases this tenant
    owns. Mirrors inventory.filter_for_company: under "my-company" scope
    we show ONLY company-owned content, not community-owned (community
    shows up under "all" scope). Empty/missing tags treated as
    "community" so legacy rows are explicitly community-scoped.
    """
    d = _uc_dir(workspace)
    out: list[UseCase] = []
    for p in d.glob("*.json"):
        try:
            data = json.loads(p.read_text(encoding="utf-8"))
            cleaned = {k: v for k, v in data.items() if k in _known()}
            uc = UseCase(**cleaned)
            if vetted_only and not uc.vetted:
                continue
            if viewer_company_id:
                owning = (uc.owning_company_id or "").strip() or "community"
                if owning != viewer_company_id:
                    continue
            out.append(uc)
        except Exception:
            pass
    if sort == "rating":
        out.sort(key=lambda u: (-u.rating_avg, -u.rating_count))
    elif sort == "persona":
        out.sort(key=lambda u: u.persona.lower())
    else:
        out.sort(key=lambda u: u.created_at, reverse=True)
    return out[:limit] if limit else out


def count(workspace: str = "global",
              viewer_company_id: str | None = None) -> int:
    """Total visible cases. When viewer_company_id is None, returns the
    raw file count (cheap, no JSON parsing). When set, applies the same
    tenant filter as list_all so the badge in the library nav matches
    what the page actually renders."""
    if viewer_company_id is None:
        return len(list(_uc_dir(workspace).glob("*.json")))
    return len(list_all(workspace, viewer_company_id=viewer_company_id))


def find_by_tool(workspace: str, category: str, asset_id: str) -> list[UseCase]:
    """Return use cases that reference this tool. O(N) scan; fine for now."""
    out = []
    for uc in list_all(workspace):
        for t in uc.tools_used:
            if t.get("category") == category and t.get("asset_id") == asset_id:
                out.append(uc); break
    return out


def new_id() -> str:
    # 10 hex chars, no dashes — keeps URLs clean
    return "uc-" + str(uuid.uuid4()).replace("-", "")[:10]


# ── Ratings + comments — mirror the asset store pattern ──────────────


def _ratings_file(workspace: str, uc_id: str) -> Path:
    return _uc_dir(workspace) / f"{uc_id}.ratings.jsonl"


def _comments_file(workspace: str, uc_id: str) -> Path:
    return _uc_dir(workspace) / f"{uc_id}.comments.jsonl"


def add_rating(workspace: str, uc_id: str, rater: str, stars: int,
                  note: str = "") -> None:
    stars = max(1, min(5, int(stars)))
    row = {"rating_id": str(uuid.uuid4())[:8], "uc_id": uc_id,
              "rater": rater, "stars": stars, "note": note,
              "created_at": _now()}
    with _ratings_file(workspace, uc_id).open("a", encoding="utf-8") as f:
        f.write(json.dumps(row) + "\n")
    # update aggregate
    rows = list_ratings(workspace, uc_id)
    uc = get(workspace, uc_id)
    if uc:
        uc.rating_avg = round(sum(r["stars"] for r in rows) / len(rows), 2) if rows else 0.0
        uc.rating_count = len(rows)
        save(uc)


def list_ratings(workspace: str, uc_id: str) -> list[dict]:
    p = _ratings_file(workspace, uc_id)
    if not p.exists():
        return []
    out = []
    for line in p.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        try: out.append(json.loads(line))
        except: pass
    return out


def add_comment(workspace: str, uc_id: str, author: str, body: str) -> None:
    row = {"comment_id": str(uuid.uuid4())[:8], "uc_id": uc_id,
              "author": author, "body": body.strip(), "created_at": _now()}
    with _comments_file(workspace, uc_id).open("a", encoding="utf-8") as f:
        f.write(json.dumps(row) + "\n")
    uc = get(workspace, uc_id)
    if uc:
        uc.comment_count = len(list_comments(workspace, uc_id))
        save(uc)


def list_comments(workspace: str, uc_id: str) -> list[dict]:
    p = _comments_file(workspace, uc_id)
    if not p.exists():
        return []
    out = []
    for line in p.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        try: out.append(json.loads(line))
        except: pass
    return out


def mark_vetted(workspace: str, uc_id: str, vetter: str, notes: str = "") -> None:
    uc = get(workspace, uc_id)
    if uc:
        uc.vetted = True
        uc.vetted_by = vetter
        uc.vetted_at = _now()
        uc.vetted_notes = notes
        save(uc)
